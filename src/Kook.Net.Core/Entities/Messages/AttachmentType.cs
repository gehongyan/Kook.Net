namespace Kook;

/// <summary>
///     表示附件的类型。
/// </summary>
public enum AttachmentType
{
    /// <summary>
    ///     附件是一份文件。
    /// </summary>
    File,

    /// <summary>
    ///     附件是一张图片。
    /// </summary>
    Image,

    /// <summary>
    ///     附件是一段视频。
    /// </summary>
    Video,

    /// <summary>
    ///     附件是一段音频。
    /// </summary>
    Audio
}
