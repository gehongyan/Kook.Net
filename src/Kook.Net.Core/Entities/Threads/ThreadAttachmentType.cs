namespace Kook;

/// <summary>
///     表示一个帖子内的附件类型。
/// </summary>
public enum ThreadAttachmentType
{
    /// <summary>
    ///     附件是一张图片。
    /// </summary>
    Image = 2,

    /// <summary>
    ///     附件是一段视频。
    /// </summary>
    Video = 3,

    /// <summary>
    ///     附件是一份文件。
    /// </summary>
    File = 4
}
