                        grouping => grouping.Key,
                        grouping => grouping.Select(kvp => kvp.Key)
                            .ToList()
                            .AsReadOnly()
                    );
            return _unicodesAndNames;
        }
    }

    /// <inheritdoc cref="M:Kook.Emoji.Parse(System.String)" />
    public static implicit operator Emoji(string emojiStr) => Parse(emojiStr);
